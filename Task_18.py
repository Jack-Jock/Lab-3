"""
18. Дано ціле число N (> 0). Використовуючи операції ділення без остачі і взяття залишку від
ділення, вивести всі його цифри, починаючи з самої правої (розряду одиниць).
Скорий Євген 141
"""

n = int(input("Введіть число N(N > 0) = "))
s = 0
if n < 0:
    print("Помилка: число повинно бути більше 0")
    exit()
else:
    print("Цифри числа починаючи з кінця")
    while n != 0:
        s = n % 10
        n = (n - s) // 10
        print(s)